package com.lokesh;

public class RelationalExample {

	public static void main(String[] args) {
		System.out.println(2.0 == 2.0);
		System.out.println(4 != 4);
		System.out.println(3 < 4);
		System.out.println(3 <= 3);
		System.out.println(3 > 4);
		System.out.println(4 >= 4);
	}
}

/*
 * Output
 * 
 * true
 * false
 * true
 * true
 * false
 * true
 */
