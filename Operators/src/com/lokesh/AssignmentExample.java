package com.lokesh;

public class AssignmentExample {
	public static void main(String[] args)    {
		int a=4;
		a+=10; // a=a+10 = 4+10 = 14     
		System.out.println(a);    
		int b=3;    
		b-=4; // b=b-4 = 3-4 = -1    
		System.out.println(b);     
		int c=7;     
		c*=2; // c=c*2 = 7*2 = 14     
		System.out.println(c);    
		int d=6;     
		d/=3; // d=d/3 = 6/3 =2     
		System.out.println(d);     
		int e=23;     
		e%=2; // e=e%2 = 23%2 = 1     
		System.out.println(e);  
	}
}

/*
 * Output
 * -------
 * 
 * 14
 * -1
 * 14
 * 2
 * 1
 * 
 */
