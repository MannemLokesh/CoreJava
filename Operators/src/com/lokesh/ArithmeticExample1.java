package com.lokesh;

public class ArithmeticExample1 {
	public static void main(String[] args) {
		System.out.println(2 + 3);
		System.out.println(2 - 3);
		System.out.println(4 * 6);
		System.out.println(33 / 2);
		System.out.println(33 % 2);
	}
}


