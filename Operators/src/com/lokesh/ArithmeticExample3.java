package com.lokesh;

public class ArithmeticExample3 {
	public static void main(String[] args) {
		int a = 27;
		int b = 2;
		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(a / b);
		System.out.println(a % b);
	}
}

/*
 * Output 
 * ------
 * 
 * 29
 * 25
 * 54
 * 13
 * 1
 */
