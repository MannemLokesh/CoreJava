package com.lokesh;

public class ArithmeticExample4 {
	public static void main(String[] args) {
		int a = 27;
		int b = 2;
		double c = a / b;
		System.out.println(c);
		double d = 33.0 / 2;
		System.out.println(d);
		double f = 33.0 / b;
		System.out.println(f);
		double g = 33 / 2.0;
		System.out.println(g);
		double h = 39.2;
		double i = 2.23;
		double j = h / i;
		System.out.println(j);
		System.out.println(h / i);
	}
}

